<h2>EMV-Nahfeldscanner (Bachelorarbeit)</h2>
<div class="carousel__textContainer">
    <div class="carousel__textHeader">Beschreibung:</div>
    <div class="carousel__textContent">Bau und Programmierung eines EMV-Nahfeldscanners zur Lokalisierung von Komponenten, die EMV Störungen verursachen.</div>
    <div class="carousel__textHeader">Aufgabenstellung:</div>
    <div class="carousel__textContent">
        <ul>
            <li>Konstruktion und Aufbau einer XY-Einheit zur Positionierung der Nahfeldsonde über dem Prüfling</li>
            <li>Entwicklung und Aufbau der elektrischen Ansteuerung</li>
            <li>Software zur Ansteuerung der XY-Einheit zum Abfahren parametrierbarer Rasterpunkte</li>
            <li>Ansteuerung eines Spektrumanalysators zur Konfiguration und Rücklesung der Messwerte</li>
            <li>Software zur Aufbereitung und Darstellung der Messergebnisse in einer farbigen 2D-Darstellung</li>
        </ul>
    </div>
